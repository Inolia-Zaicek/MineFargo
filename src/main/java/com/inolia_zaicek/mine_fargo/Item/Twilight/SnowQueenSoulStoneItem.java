@SuppressWarnings({"all", "removal"})
public class SnowQueenSoulStoneItem extends Item implements ICurioItem, TwilightST,TwilightLichST {
    public SnowQueenSoulStoneItem() {super((new Properties()).stacksTo(1).fireResistant());}
    protected String getTooltipItemName() {
        return BuiltInRegistries.ITEM.getKey(this).getPath();
    }
    @Override
    public void appendHoverText(ItemStack pStack, @Nullable Level pLevel, List<Component> pTooltipComponents, TooltipFlag pIsAdvanced) {
        String itemName = getTooltipItemName();
        pTooltipComponents.add(Component.translatable("tooltip." + "mine_fargo" + "." + itemName + ".text",
                (int)(MyGoConfig.snow_queen_soul_stone_range.get()*1),(int)(MyGoConfig.snow_queen_soul_stone_range.get()*1),
                (float)(MyGoConfig.snow_queen_soul_stone_damage.get()*100),(float)(MyGoConfig.snow_queen_soul_stone_cooldown.get()*1)
        ).withStyle(style -> style.withColor(ChatFormatting.GRAY)));
        super.appendHoverText(pStack, pLevel, pTooltipComponents, pIsAdvanced);
    }
    @Override
    public boolean canEquip(SlotContext slotContext, ItemStack stack) {
        return ! MyGoUtil.hasTwilight(slotContext.entity(), SnowQueenSoulStoneItem.class);
    }
}
